function onCad() {
    var x = document.getElementById("dropdown-cad").style.display;
    if (x == "block") {
        document.getElementById("dropdown-cad").style.display = "none";
    }
    else {
        document.getElementById("dropdown-cad").style.display = "block";
    }
}
function onFin() {
    var x = document.getElementById("dropdown-fin").style.display;
    if (x == "block") {
        document.getElementById("dropdown-fin").style.display = "none";
    }
    else {
        document.getElementById("dropdown-fin").style.display = "block";
    }
}